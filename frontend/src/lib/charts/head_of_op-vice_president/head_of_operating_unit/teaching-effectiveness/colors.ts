/**
 * Generates border colors from fill colors by making them more opaque
 * @param fillColors Array of RGBA colors
 * @returns Array of border colors with full opacity
 */
export function generateBorderColors(fillColors: string[]): string[] {
	return fillColors.map((color) => {
		// Replace the alpha value to make the border more opaque
		return color.replace(/\d?\.?\d+\)$/, '1)');
	});
}
